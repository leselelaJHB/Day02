#include <unistd.h>

void    ft_putchar(char c)
{
	write(1, &c, 1);
}
void    ft_print_numbers(void)
{
	int b;

	b = '0';
	while (b <= '9')
    {
		ft_putchar(b);
		b-=-1;
	}

}
